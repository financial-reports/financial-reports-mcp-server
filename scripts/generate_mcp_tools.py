def snake_case(s: str) -> str:
    s = re.sub(r"(?<!^)(?=[A-Z])", "_", s).lower()
    return re.sub(r"[^a-z0-9_]+", "_", s).strip("_")


def resolve_ref(full_schema: dict, ref: str) -> dict:
    if not ref.startswith("#/"):
        return {}
    current: Any = full_schema
    for part in ref[2:].split("/"):
        current = current.get(part, {}) if isinstance(current, dict) else {}
    return current


def get_python_type(schema_type, schema_format=None, is_required=True, default=None):
    py_type = "Any"
    if schema_type == "integer":
        py_type = "int"
    elif schema_type == "number":
        py_type = "float"
    elif schema_type == "string":
        py_type = "str"
    elif schema_type == "boolean":
        py_type = "bool"
    elif schema_type == "array":
        py_type = "list"
    elif schema_type == "object":
        py_type = "dict"

    default_val = ""
    if not is_required:
        py_type = f"{py_type} | None"
        default_val = " = None"

    if default is not None:
        if schema_type == "string":
            default_val = f' = "{default}"'
        else:
            default_val = f" = {default}"

    if is_required and default is None:
        default_val = ""

    return py_type, default_val


def sanitize_description(d: str) -> str:
    """Make an OpenAPI description safe to embed in a Python docstring."""
    if not d:
        return "No description available."
    d = d.strip().replace('"""', "'''")
    if len(d) > 1500:
        d = d[:1500].rstrip() + "..."
    # Collapse runs of whitespace but preserve single newlines for readability
    d = re.sub(r"[ \t]+", " ", d)
    return d


def render_tags(operation: dict) -> str:
    """Render OpenAPI tags as a Python set literal for FastMCP."""
    tags = operation.get("tags", []) or []
    if not tags:
        return "set()"
    quoted = ", ".join(f'"{t}"' for t in tags)
    return "{" + quoted + "}"


def extract_path_params(operation: dict) -> list:
    params = []
    for param in operation.get("parameters", []):
        if param.get("in") != "path":
            continue
        name = param["name"]
        param_schema = param.get("schema", {})
        py_type, default_val = get_python_type(
            param_schema.get("type"),
            param_schema.get("format"),
            param.get("required", True),
            param_schema.get("default"),
        )
        params.append({
            "name": snake_case(name),
            "original_name": name,
            "py_type": py_type,
            "default_val": default_val,
            "is_path": True,
            "is_query": False,
            "is_body": False,
        })
    return params


def extract_body_params(operation: dict, full_schema: dict) -> list:
    params = []
    request_body = operation.get("requestBody", {})
    json_schema = request_body.get("content", {}).get("application/json", {}).get("schema", {})

    if "$ref" in json_schema:
        json_schema = resolve_ref(full_schema, json_schema["$ref"])

    properties = json_schema.get("properties", {})
    required_fields = set(json_schema.get("required", []))

    for prop_name, prop_schema in properties.items():
        if "$ref" in prop_schema:
            prop_schema = resolve_ref(full_schema, prop_schema["$ref"])

        py_type, default_val = get_python_type(
            prop_schema.get("type"),
            prop_schema.get("format"),
            prop_name in required_fields,
            prop_schema.get("default"),
        )
        params.append({
            "name": snake_case(prop_name),
            "original_name": prop_name,
            "py_type": py_type,
            "default_val": default_val,
            "is_path": False,
            "is_query": False,
            "is_body": True,
        })
    return params


def extract_query_params(operation: dict) -> list:
    params = []
    for param in operation.get("parameters", []):
        if param.get("in") != "query":
            continue
        name = param["name"]
        param_schema = param.get("schema", {})
        py_type, default_val = get_python_type(
            param_schema.get("type"),
            param_schema.get("format"),
            param.get("required", False),
            param_schema.get("default"),
        )
        params.append({
            "name": snake_case(name),
            "original_name": name,
            "py_type": py_type,
            "default_val": default_val,
            "is_path": False,
            "is_query": True,
            "is_body": False,
        })
    return params


def compute_post_annotations(func_name: str, path: str) -> str:
    parts = ["readOnlyHint=False"]
    if "bulk_remove" in func_name or "bulk-remove" in path or "destroy" in func_name:
        parts.append("destructiveHint=True")
    if "replay" in func_name or "replay" in path:
        parts.append("openWorldHint=True")
    return ", ".join(parts)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    schema = None
    last_exc = None
    for attempt in range(1, 4):
        try:
            print(f"Fetching schema (attempt {attempt}/3)...", file=sys.stderr)
            response = httpx.get(SCHEMA_URL, timeout=120.0)
            response.raise_for_status()
            schema = yaml.safe_load(response.content)
            break
        except Exception as exc:
            last_exc = exc
            print(f"  attempt {attempt} failed: {exc}", file=sys.stderr)
            if attempt < 3:
                import time
                time.sleep(5 * attempt)
    if schema is None:
        print(f"Failed to download or parse schema after 3 attempts: {last_exc}", file=sys.stderr)
        sys.exit(1)

    env = jinja2.Environment(trim_blocks=False, lstrip_blocks=False)
    get_template = env.from_string(GET_TOOL_TEMPLATE)
    post_template = env.from_string(POST_TOOL_TEMPLATE)
    markdown_template = env.from_string(MARKDOWN_TOOL_TEMPLATE)

    generated_code = [FILE_HEADER_TEMPLATE]

    paths = schema.get("paths", {})
    tool_count = 0
    skipped = 0

    for path, path_item in paths.items():
        for method in ("get", "post"):
            if method not in path_item:
                continue

            operation = path_item[method]
            operation_id = operation.get("operationId")
            if not operation_id:
                skipped += 1
                continue

            func_name = snake_case(operation_id)
            description = sanitize_description(
                operation.get("description") or operation.get("summary") or ""
            )
            tags = render_tags(operation)

            if method == "get":
                # Special-case the markdown retrieve endpoint — needs pagination
                if func_name == "filings_markdown_retrieve":
                    generated_code.append(
                        markdown_template.render(
                            func_name=func_name,
                            description=description,
                        )
                    )
                    tool_count += 1
                    continue

                params = extract_path_params(operation) + extract_query_params(operation)
                # Required params first (no default), then optional
                params.sort(key=lambda p: p["default_val"] != "")

                generated_code.append(
                    get_template.render(
                        func_name=func_name,
                        description=description,
                        params=params,
                        path=path,
                        tags=tags,
                    )
                )
                tool_count += 1

            elif method == "post":
                path_params = extract_path_params(operation)
                body_params = extract_body_params(operation, schema)
                path_param_names = {p["name"] for p in path_params}
                deduped_body = [p for p in body_params if p["name"] not in path_param_names]
                all_params = path_params + sorted(
                    deduped_body, key=lambda p: p["default_val"] != ""
                )
                annotations_str = compute_post_annotations(func_name, path)

                generated_code.append(
                    post_template.render(
                        func_name=func_name,
                        description=description,
                        params=all_params,
                        path=path,
                        tags=tags,
                        annotations_str=annotations_str,
                    )
                )
                tool_count += 1

    generated_code.append(FILE_FOOTER)

    OUTPUT_FILE.parent.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        f.write("\n".join(generated_code))

    print(f"✅ Generated {tool_count} tools at {OUTPUT_FILE}")
    if skipped:
        print(f"⚠️  Skipped {skipped} operations with no operationId")


if __name__ == "__main__":
    main()
