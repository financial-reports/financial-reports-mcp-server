def _backend_client() -> httpx.AsyncClient:
    """Build an httpx client authenticated as the current MCP user.

    The token is forwarded to api.financialreports.eu, where Django's
    CognitoJWTAuthentication validates it against the same Cognito pool —
    so the user hits API endpoints as themselves and consumes their own quota.
    """
    token = _current_token.get()
    if not token:
        raise RuntimeError("No token in context — tool called outside subscription_required?")
    return httpx.AsyncClient(
        base_url=API_BASE_URL,
        headers={
            "Authorization": f"Bearer {token}",
            "User-Agent": f"FinancialReports-MCP/{MCP_VERSION}",
        },
        timeout=60.0,
    )


async def _format_response(response: httpx.Response) -> str:
    """Format an httpx.Response as a JSON code block for the LLM."""
    try:
        response.raise_for_status()
        data = response.json()
        return f"```json\\n{_json.dumps(data, indent=2, ensure_ascii=False)}\\n```"
    except httpx.HTTPStatusError as exc:
        return (
            f"Error {exc.response.status_code} {exc.response.reason_phrase}: "
            f"{exc.response.text[:500]}"
        )
    except Exception as exc:
        return f"Error formatting response: {exc}"


