def main() -> None:
    schema = None
    last_exc: Exception | None = None
    for attempt in range(1, 4):
        try:
            print(f"Fetching schema (attempt {attempt}/3)...", file=sys.stderr)
            response = httpx.get(
                SCHEMA_URL,
                timeout=120.0,
                headers={"User-Agent": "FinancialReports-MCP-Generator/1.0"},
            )
            response.raise_for_status()
            schema = yaml.safe_load(response.content)
            break
        except Exception as exc:
            last_exc = exc
            print(f"  attempt {attempt} failed: {exc}", file=sys.stderr)
            if attempt < 3:
                time.sleep(5 * attempt)
    if schema is None:
        print(
            f"Failed to download or parse schema after 3 attempts: {last_exc}",
            file=sys.stderr,
        )
        sys.exit(1)

    env = jinja2.Environment(trim_blocks=False, lstrip_blocks=False)
    get_template = env.from_string(GET_TOOL_TEMPLATE)
    structured_get_template = env.from_string(STRUCTURED_GET_TOOL_TEMPLATE)
    post_template = env.from_string(POST_TOOL_TEMPLATE)
    markdown_template = env.from_string(MARKDOWN_TOOL_TEMPLATE)
    structured_count = 0
    missing_response_schema: list[str] = []

    overrides = load_tool_overrides()

    generated_code = [FILE_HEADER_TEMPLATE]
    generated_tools: list[dict] = []

    paths = schema.get("paths", {})
    tool_count = 0
    skipped_no_id = 0
    skipped_methods: list[str] = []

    for path, path_item in paths.items():
        for method, operation in path_item.items():
            if not isinstance(operation, dict):
                continue
            if method.lower() in ("get", "post"):
                continue
            if method.lower() in ("delete", "patch", "put"):
                op_id = operation.get("operationId") or "<unnamed>"
                skipped_methods.append(f"{method.upper()} {path} ({op_id})")

        for method in ("get", "post"):
            if method not in path_item:
                continue

            operation = path_item[method]
            operation_id = operation.get("operationId")
            if not operation_id:
                skipped_no_id += 1
                continue

            func_name = snake_case(operation_id)
            description = sanitize_description(
                operation.get("description") or operation.get("summary") or ""
            )
            description = apply_overrides(func_name, description, overrides)
            tags_value = operation.get("tags", []) or []
            tags = render_tags(operation)
            # Title: prefer the OpenAPI summary (human-written, e.g. "List Companies"),
            # fall back to a Title Case version of the function name. Escape quotes.
            title_raw = (operation.get("summary") or "").strip()
            if not title_raw:
                title_raw = func_name.replace("_", " ").title()
            title = title_raw.replace('"', '\\"')

            if method == "get":
                # Special-case the markdown retrieve endpoint — needs pagination
                if func_name == "filings_markdown_retrieve":
                    generated_code.append(
                        markdown_template.render(
                            func_name=func_name,
                            description=description,
                            title=title,
                        )
                    )
                    tool_count += 1
                    generated_tools.append(
                        {"func_name": func_name, "title": title_raw, "tags": tags_value or ["Filings"]}
                    )
                    continue

                params = extract_path_params(operation) + extract_query_params(operation)
                # Required params first (no default), then optional
                params.sort(key=lambda p: p["default_val"] != "")

                # Promoted to structured output: emit `output_schema=` with
                # the OpenAPI 200-response schema deeply inlined, and use
                # the structured tool template (returns dict, raises on
                # auth failure so FastMCP surfaces it as a tool error
                # instead of trying to fit markdown into the JSON Schema).
                if func_name in STRUCTURED_OUTPUT_TOOLS:
                    response_schema = extract_response_schema(operation, schema)
                    if response_schema is None:
                        missing_response_schema.append(func_name)
                        # Fall through to the unstructured template.
                    else:
                        generated_code.append(
                            structured_get_template.render(
                                func_name=func_name,
                                description=description,
                                params=params,
                                path=path,
                                tags=tags,
                                title=title,
                                output_schema_repr=repr(response_schema),
                            )
                        )
                        tool_count += 1
                        structured_count += 1
                        generated_tools.append(
                            {
                                "func_name": func_name,
                                "title": title_raw,
                                "tags": tags_value,
                            }
                        )
                        continue

                generated_code.append(
                    get_template.render(
                        func_name=func_name,
                        description=description,
                        params=params,
                        path=path,
                        tags=tags,
                        title=title,
                    )
                )
                tool_count += 1
                generated_tools.append(
                    {"func_name": func_name, "title": title_raw, "tags": tags_value}
                )

            elif method == "post":
                path_params = extract_path_params(operation)
                body_params = extract_body_params(operation, schema)
                path_param_names = {p["name"] for p in path_params}
                deduped_body = [p for p in body_params if p["name"] not in path_param_names]
                all_params = path_params + sorted(
                    deduped_body, key=lambda p: p["default_val"] != ""
                )
                annotations_str = compute_post_annotations(func_name, path)
                has_webhook_url = post_body_has_url_field(operation, schema)

                generated_code.append(
                    post_template.render(
                        func_name=func_name,
                        description=description,
                        params=all_params,
                        path=path,
                        tags=tags,
                        annotations_str=annotations_str,
                        title=title,
                        has_webhook_url=has_webhook_url,
                    )
                )
                tool_count += 1
                generated_tools.append(
                    {"func_name": func_name, "title": title_raw, "tags": tags_value}
                )

    generated_code.append(FILE_FOOTER)

    OUTPUT_FILE.parent.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        f.write("\n".join(generated_code))

    print(f"✅ Generated {tool_count} tools at {OUTPUT_FILE}")
    if structured_count:
        print(
            f"   ↳ {structured_count} of those advertise an `output_schema` "
            f"(structured-content tools): {sorted(STRUCTURED_OUTPUT_TOOLS)}"
        )
    if missing_response_schema:
        print(
            f"⚠️  STRUCTURED_OUTPUT_TOOLS includes {missing_response_schema} "
            "but the OpenAPI doc has no JSON 200 response schema — fell back "
            "to the unstructured template.",
            file=sys.stderr,
        )
    if skipped_no_id:
        print(f"⚠️  Skipped {skipped_no_id} operations with no operationId")
    if skipped_methods:
        print(
            f"⚠️  Skipped {len(skipped_methods)} non-GET/POST operations "
            "(this generator only emits GET and POST tools):",
            file=sys.stderr,
        )
        for entry in skipped_methods[:20]:
            print(f"     - {entry}", file=sys.stderr)
        if len(skipped_methods) > 20:
            print(f"     ... and {len(skipped_methods) - 20} more", file=sys.stderr)

    update_readme_tool_table(
        OUTPUT_FILE.parent.parent, render_readme_tool_table(generated_tools)
    )


if __name__ == "__main__":
    main()
