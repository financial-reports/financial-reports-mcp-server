def deeply_inline_refs(node: Any, full_schema: dict, _seen: tuple[str, ...] = ()) -> Any:
    """Walk a JSON-Schema fragment and substitute any `$ref` with the
    referenced node, inlined.

    OpenAPI uses `$ref` everywhere; the MCP `outputSchema` field is a
    standalone JSON Schema and renderers can't always follow refs back
    into a parent document. Inlining produces a self-contained schema.

    Cycle protection: `_seen` tracks the chain of refs we're inside; if
    we hit one we've already visited, we stop expansion (replace with
    `{}` so the outer schema is still well-formed). Real OpenAPI rarely
    cycles in response shapes, but defence in depth.
    """
    if isinstance(node, dict):
        if "$ref" in node and isinstance(node["$ref"], str):
            ref = node["$ref"]
            if ref in _seen:
                # Cycle — stop here. The advertised schema becomes a
                # permissive object at this point rather than infinite.
                return {"type": "object", "additionalProperties": True}
            target = resolve_ref(full_schema, ref)
            if not target:
                return {}
            return deeply_inline_refs(target, full_schema, _seen + (ref,))
        out: dict[str, Any] = {}
        for k, v in node.items():
            if k == "$ref":
                continue
            out[k] = deeply_inline_refs(v, full_schema, _seen)
        return out
    if isinstance(node, list):
        return [deeply_inline_refs(item, full_schema, _seen) for item in node]
    return node


def _fix_nullable(node: Any) -> Any:
    """Convert OpenAPI 3.0 ``nullable: true`` to JSON Schema ``type: [T, 'null']``.

    OpenAPI 3.0 uses ``{"type": "string", "nullable": true}`` but JSON Schema
    (and MCP output validation) requires ``{"type": ["string", "null"]}``.
    """
    if isinstance(node, dict):
        out: dict[str, Any] = {}
        is_nullable = node.get("nullable") is True
        for k, v in node.items():
            if k == "nullable":
                continue
            out[k] = _fix_nullable(v)
        if is_nullable and "type" in out:
            t = out["type"]
            if isinstance(t, str):
                out["type"] = [t, "null"]
            elif isinstance(t, list) and "null" not in t:
                out["type"] = t + ["null"]
        return out
    if isinstance(node, list):
        return [_fix_nullable(item) for item in node]
    return node


def _relax_nullable_required(node: Any) -> Any:
    """Drop nullable properties from every object's ``required`` list.

    A property that can be null — or that the upstream API simply omits for some
    rows — must NOT be ``required`` in the MCP output schema. FastMCP validates
    each tool result against the schema, and a missing key for a "required" field
    hard-fails the ENTIRE call with ``'X' is a required property`` even though the
    API response is perfectly valid. Concrete bug: ``FilingSummary.document_url``
    is required+nullable, so ``filings_list`` errored on any filing with no
    document (e.g. ASML); the OpenAPI spec marks ~30 ``Company`` fields the same
    way (ticker, logo, sector, …), so nearly every structured tool could fail on a
    sparsely-populated row. Runs AFTER ``_fix_nullable`` (nullable already
    normalised to ``type: [T, "null"]``). Mutates in place; returns the node."""
    def _is_nullable(p: Any) -> bool:
        if not isinstance(p, dict):
            return False
        t = p.get("type")
        if t == "null" or (isinstance(t, list) and "null" in t):
            return True
        return any(_is_nullable(s) for s in (p.get("anyOf") or []) + (p.get("oneOf") or []))

    if isinstance(node, dict):
        props = node.get("properties")
        req = node.get("required")
        if isinstance(props, dict) and isinstance(req, list):
            kept = [r for r in req if not _is_nullable(props.get(r))]
            if kept:
                node["required"] = kept
            else:
                node.pop("required", None)
        for v in node.values():
            _relax_nullable_required(v)
    elif isinstance(node, list):
        for it in node:
            _relax_nullable_required(it)
    return node


def _make_fields_nullable(node: Any) -> Any:
    """Make every typed node in the output schema tolerate ``null``.

    The upstream API serializer returns ``null`` for fields the OpenAPI spec
    declares NON-nullable — e.g. ``ListedStockExchange.ticker`` / ``.website`` for
    whole exchanges (Shenzhen, Saudi, Shanghai, NSE India, Abu Dhabi…). FastMCP
    output validation then hard-fails with ``None is not of type 'string'`` and
    rejects the ENTIRE result, breaking ``companies_retrieve`` for every company
    listed on those exchanges. #49 dropped nullable-TYPED fields from ``required``
    but did NOT handle non-nullable fields the API nulls (a value/type violation,
    not a missing key). A thin proxy must never reject a valid upstream response,
    so add ``"null"`` to every declared ``type``: any field may be null. Run
    BEFORE ``_relax_nullable_required`` (which then clears the now-nullable fields
    from ``required`` too). Mutates in place; returns the node."""
    if isinstance(node, dict):
        # Combinator nodes (oneOf/anyOf/allOf) already express nullability via a
        # {"type":"null"} branch. Adding "null" to a sibling branch would make null
        # match MULTIPLE branches and break oneOf validation ("None is valid under
        # each of …", seen on `legal_form`). Leave those constructs untouched and
        # don't recurse into their branch lists; only widen plain typed nodes.
        # Strip `format` assertions (e.g. "uri"): the upstream API returns empty
        # strings for `format:uri` fields like ir_link/website, which jsonschema
        # tolerates (format is advisory) but a strict Pydantic-based MCP client
        # rejects ("Input should be a valid URL, input is empty"). A thin proxy
        # must not assert a constraint the upstream data violates.
        node.pop("format", None)
        has_combinator = any(k in node for k in ("oneOf", "anyOf", "allOf"))
        if not has_combinator:
            t = node.get("type")
            if isinstance(t, str) and t != "null":
                node["type"] = [t, "null"]
            elif isinstance(t, list) and "null" not in t:
                node["type"] = t + ["null"]
        for k, v in node.items():
            if k in ("oneOf", "anyOf", "allOf"):
                continue
            _make_fields_nullable(v)
    elif isinstance(node, list):
        for it in node:
            _make_fields_nullable(it)
    return node


def _strip_hidden_from_schema(node):
    """Remove every `CLIENT_HIDDEN_FIELDS` key from any `properties` map in a
    JSON-Schema dict (and from any sibling `required` list), recursively.

    Keeps the emitted `output_schema` consistent with what `_scrub_response`
    actually returns: a field that's stripped at runtime is never advertised
    in `tools/list`. Mutates in place — callers pass a deep copy so the shared
    inlined snapshot schema is never corrupted. Returns `node`."""
    if isinstance(node, dict):
        props = node.get("properties")
        if isinstance(props, dict):
            for _hidden in CLIENT_HIDDEN_FIELDS:
                props.pop(_hidden, None)
            req = node.get("required")
            if isinstance(req, list):
                node["required"] = [r for r in req if r not in CLIENT_HIDDEN_FIELDS]
        for _v in node.values():
            _strip_hidden_from_schema(_v)
    elif isinstance(node, list):
        for _item in node:
            _strip_hidden_from_schema(_item)
    return node


def extract_response_schema(operation: dict, full_schema: dict) -> dict | None:
    """Return the `application/json` 200-response JSON Schema for an
    operation, with all `$ref`s deeply inlined. None if the operation
    declares no JSON 200 response."""
    responses = operation.get("responses") or {}
    ok = responses.get("200") or responses.get(200)
    if not isinstance(ok, dict):
        return None
    json_content = (ok.get("content") or {}).get("application/json") or {}
    raw_schema = json_content.get("schema")
    if not raw_schema:
        return None
    inlined = deeply_inline_refs(raw_schema, full_schema)
    schema = _relax_nullable_required(_make_fields_nullable(_fix_nullable(inlined)))
    # The ROOT of a structured-output schema MUST be exactly type "object" — a tool
    # returning structured content returns an object, never null. _make_fields_nullable
    # widens every NESTED field to tolerate upstream nulls (the #49/#50 fix), but it also
    # widened the root to ["object","null"], which the MCP spec / Anthropic SDK reject
    # (outputSchema.type is the literal "object"). Strict clients (Claude Code) then fail
    # the ENTIRE tools/list with "tools fetch failed" (Claude Desktop is lenient and
    # accepts the union, which is why it worked there). Coerce the root back to "object".
    if isinstance(schema, dict) and isinstance(schema.get("type"), list):
        schema["type"] = "object"
    return schema


def get_python_type(
    schema_type,
    schema_format=None,
    is_required=True,
    default=None,
    enum=None,
    items_type=None,
):
    if enum and schema_type == "string":
        quoted = ", ".join(f'"{v}"' for v in enum)
        py_type = f"Literal[{quoted}]"
    elif schema_type == "integer":
        py_type = "int"
    elif schema_type == "number":
        py_type = "float"
    elif schema_type == "string":
        py_type = "str"
    elif schema_type == "boolean":
        py_type = "bool"
    elif schema_type == "array":
        if items_type == "integer":
            py_type = "list[int]"
        elif items_type == "string":
            py_type = "list[str]"
        else:
            py_type = "list"
    elif schema_type == "object":
        py_type = "dict"
    else:
        py_type = "Any"

    default_val = ""
    if not is_required:
        py_type = f"{py_type} | None"
        default_val = " = None"

    if default is not None:
        if schema_type == "string":
            default_val = f' = "{default}"'
        else:
            default_val = f" = {default}"

    if is_required and default is None:
        default_val = ""

    return py_type, default_val


def sanitize_description(d: str) -> str:
    """Make an OpenAPI description safe to embed in a Python docstring."""
    if not d:
        return "No description available."
    d = d.strip().replace('"""', "'''")
    # Strip "Access Level Required" boilerplate — access tiers are an API
    # concept that doesn't apply to the MCP connector (free for all accounts).
    d = re.sub(
        r"\*\*Access Level Required:\*\*[^\n]*",
        "",
        d,
    )
    # Strip leftover horizontal-rule separators.
    d = re.sub(r"^\s*---\s*$", "", d, flags=re.MULTILINE)
    d = d.strip()
    if not d:
        return "No description available."
    if len(d) > 1500:
        d = d[:1500].rstrip() + "..."
    # Collapse runs of whitespace but preserve single newlines for readability
    d = re.sub(r"[ \t]+", " ", d)
    return d


def load_tool_overrides() -> dict[str, Any]:
    """Load per-tool description overrides from tool_overrides.yaml."""
    if not OVERRIDES_FILE.exists():
        return {}
    with open(OVERRIDES_FILE, encoding="utf-8") as f:
        return yaml.safe_load(f) or {}


def _docstring_safe(s: str) -> str:
    """Escape text so it's safe inside a triple-quoted Python docstring."""
    return s.replace('"""', "'''").replace("\\", "\\\\")


def apply_overrides(func_name: str, description: str, overrides: dict[str, Any]) -> str:
    """Merge 'when to use', 'when not to use', and examples into a description."""
    entry = overrides.get(func_name)
    if not entry:
        return description
    parts = [description.rstrip()]
    if entry.get("when_to_use"):
        parts.append(f"\n\n**When to use this tool:** {_docstring_safe(entry['when_to_use'].strip())}")
    if entry.get("when_not_to_use"):
        parts.append(f"\n\n**When NOT to use this tool:** {_docstring_safe(entry['when_not_to_use'].strip())}")
    if entry.get("examples"):
        parts.append("\n\n**Examples:**")
        for ex in entry["examples"]:
            params = _docstring_safe(ex["parameters"])
            line = f"\n- {_docstring_safe(ex['user_says'])} -> {params}"
            if ex.get("notes"):
                line += f" ({_docstring_safe(ex['notes'])})"
            parts.append(line)
    return "".join(parts)


def render_tags(operation: dict) -> str:
    """Render OpenAPI tags as a Python set literal for FastMCP."""
    tags = operation.get("tags", []) or []
    if not tags:
        return "set()"
    quoted = ", ".join(f'"{t}"' for t in tags)
    return "{" + quoted + "}"


def extract_path_params(operation: dict) -> list:
    params = []
    for param in operation.get("parameters", []):
        if param.get("in") != "path":
            continue
        name = param["name"]
        param_schema = param.get("schema", {})
        py_type, default_val = get_python_type(
            param_schema.get("type"),
            param_schema.get("format"),
            param.get("required", True),
            param_schema.get("default"),
            enum=param_schema.get("enum"),
            items_type=(param_schema.get("items") or {}).get("type"),
        )
        params.append({
            "name": snake_case(name),
            "original_name": name,
            "py_type": py_type,
            "default_val": default_val,
            "is_path": True,
            "is_query": False,
            "is_body": False,
        })
    return params


def extract_body_params(operation: dict, full_schema: dict) -> list:
    params = []
    request_body = operation.get("requestBody", {})
    json_schema = request_body.get("content", {}).get("application/json", {}).get("schema", {})

    if "$ref" in json_schema:
        json_schema = resolve_ref(full_schema, json_schema["$ref"])

    properties = json_schema.get("properties", {})
    required_fields = set(json_schema.get("required", []))

    for prop_name, prop_schema in properties.items():
        if "$ref" in prop_schema:
            prop_schema = resolve_ref(full_schema, prop_schema["$ref"])

        if prop_schema.get("readOnly"):
            continue

        py_type, default_val = get_python_type(
            prop_schema.get("type"),
            prop_schema.get("format"),
            prop_name in required_fields,
            prop_schema.get("default"),
            enum=prop_schema.get("enum"),
            items_type=(prop_schema.get("items") or {}).get("type"),
        )
        params.append({
            "name": snake_case(prop_name),
            "original_name": prop_name,
            "py_type": py_type,
            "default_val": default_val,
            "is_path": False,
            "is_query": False,
            "is_body": True,
        })
    return params


