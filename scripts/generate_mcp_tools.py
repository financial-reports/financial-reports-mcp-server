def snake_case(s: str) -> str:
    s = re.sub(r"(?<!^)(?=[A-Z])", "_", s).lower()
    return re.sub(r"[^a-z0-9_]+", "_", s).strip("_")


def resolve_ref(full_schema: dict, ref: str) -> dict:
    if not ref.startswith("#/"):
        return {}
    current: Any = full_schema
    for part in ref[2:].split("/"):
        current = current.get(part, {}) if isinstance(current, dict) else {}
    return current


# Tools whose response shape is well-defined and stable enough to commit
# to as a server-advertised JSON Schema. The MCP 2025-11-25 spec MAKES
# `outputSchema` optional but, when declared, mandates that returned
# results conform — so we keep this list narrow on purpose. Adding new
# tools here is a deliberate spec-contract choice: new tools may be
# added freely with markdown output (no schema) and only get promoted
# to the structured set once their response shape is reviewed.
STRUCTURED_OUTPUT_TOOLS = {
    "companies_list",
    "companies_retrieve",
    "companies_financials_retrieve",
    "filings_list",
    "filings_retrieve",
    "isins_list",
}


def deeply_inline_refs(node: Any, full_schema: dict, _seen: tuple[str, ...] = ()) -> Any:
    """Walk a JSON-Schema fragment and substitute any `$ref` with the
    referenced node, inlined.

    OpenAPI uses `$ref` everywhere; the MCP `outputSchema` field is a
    standalone JSON Schema and renderers can't always follow refs back
    into a parent document. Inlining produces a self-contained schema.

    Cycle protection: `_seen` tracks the chain of refs we're inside; if
    we hit one we've already visited, we stop expansion (replace with
    `{}` so the outer schema is still well-formed). Real OpenAPI rarely
    cycles in response shapes, but defence in depth.
    """
    if isinstance(node, dict):
        if "$ref" in node and isinstance(node["$ref"], str):
            ref = node["$ref"]
            if ref in _seen:
                # Cycle — stop here. The advertised schema becomes a
                # permissive object at this point rather than infinite.
                return {"type": "object", "additionalProperties": True}
            target = resolve_ref(full_schema, ref)
            if not target:
                return {}
            return deeply_inline_refs(target, full_schema, _seen + (ref,))
        out: dict[str, Any] = {}
        for k, v in node.items():
            if k == "$ref":
                continue
            out[k] = deeply_inline_refs(v, full_schema, _seen)
        return out
    if isinstance(node, list):
        return [deeply_inline_refs(item, full_schema, _seen) for item in node]
    return node


def _fix_nullable(node: Any) -> Any:
    """Convert OpenAPI 3.0 ``nullable: true`` to JSON Schema ``type: [T, 'null']``.

    OpenAPI 3.0 uses ``{"type": "string", "nullable": true}`` but JSON Schema
    (and MCP output validation) requires ``{"type": ["string", "null"]}``.
    """
    if isinstance(node, dict):
        out: dict[str, Any] = {}
        is_nullable = node.get("nullable") is True
        for k, v in node.items():
            if k == "nullable":
                continue
            out[k] = _fix_nullable(v)
        if is_nullable and "type" in out:
            t = out["type"]
            if isinstance(t, str):
                out["type"] = [t, "null"]
            elif isinstance(t, list) and "null" not in t:
                out["type"] = t + ["null"]
        return out
    if isinstance(node, list):
        return [_fix_nullable(item) for item in node]
    return node


def extract_response_schema(operation: dict, full_schema: dict) -> dict | None:
    """Return the `application/json` 200-response JSON Schema for an
    operation, with all `$ref`s deeply inlined. None if the operation
    declares no JSON 200 response."""
    responses = operation.get("responses") or {}
    ok = responses.get("200") or responses.get(200)
    if not isinstance(ok, dict):
        return None
    json_content = (ok.get("content") or {}).get("application/json") or {}
    raw_schema = json_content.get("schema")
    if not raw_schema:
        return None
    inlined = deeply_inline_refs(raw_schema, full_schema)
    return _fix_nullable(inlined)


def get_python_type(
    schema_type,
    schema_format=None,
    is_required=True,
    default=None,
    enum=None,
    items_type=None,
):
    if enum and schema_type == "string":
        quoted = ", ".join(f'"{v}"' for v in enum)
        py_type = f"Literal[{quoted}]"
    elif schema_type == "integer":
        py_type = "int"
    elif schema_type == "number":
        py_type = "float"
    elif schema_type == "string":
        py_type = "str"
    elif schema_type == "boolean":
        py_type = "bool"
    elif schema_type == "array":
        if items_type == "integer":
            py_type = "list[int]"
        elif items_type == "string":
            py_type = "list[str]"
        else:
            py_type = "list"
    elif schema_type == "object":
        py_type = "dict"
    else:
        py_type = "Any"

    default_val = ""
    if not is_required:
        py_type = f"{py_type} | None"
        default_val = " = None"

    if default is not None:
        if schema_type == "string":
            default_val = f' = "{default}"'
        else:
            default_val = f" = {default}"

    if is_required and default is None:
        default_val = ""

    return py_type, default_val


def sanitize_description(d: str) -> str:
    """Make an OpenAPI description safe to embed in a Python docstring."""
    if not d:
        return "No description available."
    d = d.strip().replace('"""', "'''")
    # Strip "Access Level Required" boilerplate — access tiers are an API
    # concept that doesn't apply to the MCP connector (free for all accounts).
    d = re.sub(
        r"\*\*Access Level Required:\*\*[^\n]*",
        "",
        d,
    )
    # Strip leftover horizontal-rule separators.
    d = re.sub(r"^\s*---\s*$", "", d, flags=re.MULTILINE)
    d = d.strip()
    if not d:
        return "No description available."
    if len(d) > 1500:
        d = d[:1500].rstrip() + "..."
    # Collapse runs of whitespace but preserve single newlines for readability
    d = re.sub(r"[ \t]+", " ", d)
    return d


def load_tool_overrides() -> dict[str, Any]:
    """Load per-tool description overrides from tool_overrides.yaml."""
    if not OVERRIDES_FILE.exists():
        return {}
    with open(OVERRIDES_FILE, encoding="utf-8") as f:
        return yaml.safe_load(f) or {}


def _docstring_safe(s: str) -> str:
    """Escape text so it's safe inside a triple-quoted Python docstring."""
    return s.replace('"""', "'''").replace("\\", "\\\\")


def apply_overrides(func_name: str, description: str, overrides: dict[str, Any]) -> str:
    """Merge 'when to use', 'when not to use', and examples into a description."""
    entry = overrides.get(func_name)
    if not entry:
        return description
    parts = [description.rstrip()]
    if entry.get("when_to_use"):
        parts.append(f"\n\n**When to use this tool:** {_docstring_safe(entry['when_to_use'].strip())}")
    if entry.get("when_not_to_use"):
        parts.append(f"\n\n**When NOT to use this tool:** {_docstring_safe(entry['when_not_to_use'].strip())}")
    if entry.get("examples"):
        parts.append("\n\n**Examples:**")
        for ex in entry["examples"]:
            params = _docstring_safe(ex["parameters"])
            line = f"\n- {_docstring_safe(ex['user_says'])} -> {params}"
            if ex.get("notes"):
                line += f" ({_docstring_safe(ex['notes'])})"
            parts.append(line)
    return "".join(parts)


def render_tags(operation: dict) -> str:
    """Render OpenAPI tags as a Python set literal for FastMCP."""
    tags = operation.get("tags", []) or []
    if not tags:
        return "set()"
    quoted = ", ".join(f'"{t}"' for t in tags)
    return "{" + quoted + "}"


def extract_path_params(operation: dict) -> list:
    params = []
    for param in operation.get("parameters", []):
        if param.get("in") != "path":
            continue
        name = param["name"]
        param_schema = param.get("schema", {})
        py_type, default_val = get_python_type(
            param_schema.get("type"),
            param_schema.get("format"),
            param.get("required", True),
            param_schema.get("default"),
            enum=param_schema.get("enum"),
            items_type=(param_schema.get("items") or {}).get("type"),
        )
        params.append({
            "name": snake_case(name),
            "original_name": name,
            "py_type": py_type,
            "default_val": default_val,
            "is_path": True,
            "is_query": False,
            "is_body": False,
        })
    return params


def extract_body_params(operation: dict, full_schema: dict) -> list:
    params = []
    request_body = operation.get("requestBody", {})
    json_schema = request_body.get("content", {}).get("application/json", {}).get("schema", {})

    if "$ref" in json_schema:
        json_schema = resolve_ref(full_schema, json_schema["$ref"])

    properties = json_schema.get("properties", {})
    required_fields = set(json_schema.get("required", []))

    for prop_name, prop_schema in properties.items():
        if "$ref" in prop_schema:
            prop_schema = resolve_ref(full_schema, prop_schema["$ref"])

        if prop_schema.get("readOnly"):
            continue

        py_type, default_val = get_python_type(
            prop_schema.get("type"),
            prop_schema.get("format"),
            prop_name in required_fields,
            prop_schema.get("default"),
            enum=prop_schema.get("enum"),
            items_type=(prop_schema.get("items") or {}).get("type"),
        )
        params.append({
            "name": snake_case(prop_name),
            "original_name": prop_name,
            "py_type": py_type,
            "default_val": default_val,
            "is_path": False,
            "is_query": False,
            "is_body": True,
        })
    return params


def extract_query_params(operation: dict) -> list:
    params = []
    for param in operation.get("parameters", []):
        if param.get("in") != "query":
            continue
        name = param["name"]
        param_schema = param.get("schema", {})
        py_type, default_val = get_python_type(
            param_schema.get("type"),
            param_schema.get("format"),
            param.get("required", False),
            param_schema.get("default"),
            enum=param_schema.get("enum"),
            items_type=(param_schema.get("items") or {}).get("type"),
        )
        params.append({
            "name": snake_case(name),
            "original_name": name,
            "py_type": py_type,
            "default_val": default_val,
            "is_path": False,
            "is_query": True,
            "is_body": False,
        })
    return params


def compute_post_annotations(func_name: str, path: str) -> str:
    """Compute extra ToolAnnotations kwargs for a POST tool.

    Per Anthropic's connector review guidance, treat all POSTs that mutate
    server state as destructiveHint=True — including creates, since the
    reviewer cares about "can this change my data" not the spec-strict
    "is this delete-shaped". webhooks_test_create is the one exception:
    it's a probe (open world) but doesn't mutate FR state.
    """
    parts: list[str] = []

    if "test_create" in func_name or "test" in func_name.split("_"):
        parts = ["destructiveHint=False", "openWorldHint=True", "idempotentHint=False"]
        return ",\n        ".join(parts)

    if "replay" in func_name:
        parts = ["destructiveHint=False", "openWorldHint=True", "idempotentHint=False"]
        return ",\n        ".join(parts)

    parts = ["destructiveHint=True", "idempotentHint=False", "openWorldHint=False"]
    return ",\n        ".join(parts)


def post_body_has_url_field(operation: dict, full_schema: dict) -> bool:
    """Detect tools that accept a `url` body field.

    Used to wire webhook URL validation into the generated POST wrapper at
    build time without coupling the runtime template to schema knowledge.
    """
    request_body = operation.get("requestBody", {})
    json_schema = (
        request_body.get("content", {}).get("application/json", {}).get("schema", {})
    )
    if "$ref" in json_schema:
        json_schema = resolve_ref(full_schema, json_schema["$ref"])
    return "url" in (json_schema.get("properties") or {})


def render_readme_tool_table(generated_tools: list[dict]) -> str:
    """Build a markdown bullet list of every generated tool, grouped by tag.

    Inserted into README.md between markers so the README cannot drift from
    the live tool surface.
    """
    by_tag: dict[str, list[tuple[str, str]]] = {}
    for tool in generated_tools:
        for tag in tool.get("tags") or ["Other"]:
            by_tag.setdefault(tag, []).append((tool["func_name"], tool["title"]))

    lines: list[str] = []
    for tag in sorted(by_tag):
        lines.append(f"### {tag}")
        for name, title in sorted(by_tag[tag]):
            lines.append(f"* `{name}` — {title}")
        lines.append("")
    return "\n".join(lines).strip() + "\n"


def update_readme_tool_table(repo_root: Path, body: str) -> None:
    readme = repo_root / "README.md"
    if not readme.exists():
        return
    text = readme.read_text(encoding="utf-8")
    start = "<!-- BEGIN AUTO-GENERATED TOOL LIST -->"
    end = "<!-- END AUTO-GENERATED TOOL LIST -->"
    if start not in text or end not in text:
        # Markers not present yet — nothing to do; README will need to be
        # edited once to add them. Avoid mangling user-curated content.
        return
    pre, _, rest = text.partition(start)
    _, _, post = rest.partition(end)
    new = (
        pre
        + start
        + "\n<!-- DO NOT EDIT BY HAND. Re-run scripts/generate_mcp_tools.py. -->\n\n"
        + body
        + "\n"
        + end
        + post
    )
    if new != text:
        readme.write_text(new, encoding="utf-8")
        print(f"📝 Updated tool list in {readme}")


